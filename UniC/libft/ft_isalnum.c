#include "libft.h"

int	ft_isalnum(int c)
{
	int	ischar;
	int	isnum;

	ischar = (c >= 'a' && c <= 'z') || (c >= 'A' && c <= 'Z');
	isnum = c >= '0' && c <= '9';
	return (ischar || isnum);
}
